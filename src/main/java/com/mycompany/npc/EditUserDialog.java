    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        if (currentUser.isAdmin()) {
            editedUser.setName(nameField.getText().trim());
            editedUser.setSurname(surnameField.getText().trim());
            String selected = roleField.getSelectedItem().toString();
            int value = selected.equals("Admin") ? 1 : 0;
            editedUser.setRole(value);
        }

        String newPassword = new String(passwordField.getPassword()).trim();
        if (newPassword.isEmpty()) {
            newPassword = null;
        }

        UserController.updateUser(editedUser, newPassword);
        dispose();
    }//GEN-LAST:event_saveBtnActionPerformed

