    private void fillFields() {
        nameField.setText(editedUser.getName());
        surnameField.setText(editedUser.getSurname());
        passwordField.setText(""); // password never display
    }
    private void applyPermissions() {
        if (currentUser.isAdmin()) {
            nameField.setEnabled(true);
            surnameField.setEnabled(true);
            roleLabel.setVisible(true);
            roleField.setVisible(true);
            
            roleField.setSelectedIndex(editedUser.isAdmin() ? 1 : 0);
        } else {
            nameField.setEnabled(false);
            surnameField.setEnabled(false);
            roleLabel.setVisible(false);
            roleField.setVisible(false);
        }
    }

